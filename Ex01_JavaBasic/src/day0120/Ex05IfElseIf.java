package day0120;

public class Ex05IfElseIf {
    public static void main(String[] args) {

    int number = 0;
    
    if(number > 0) {
        System.out.println("양의 정수입니다.");
    } else if(number == 0) {
        System.out.println("0입니다.");
    } else {
        System.out.println("음의 정수입니다.");
        }   
    }
}
